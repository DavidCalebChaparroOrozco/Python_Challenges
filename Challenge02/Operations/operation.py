operationA = 3
operationB = 2
add = operationA + operationB
print(f'Result of the sum: {add}')

subtraction = operationA - operationB
print(f'Result of the subtraction: {subtraction}')

multiplication = operationA * operationB
print(f'Result of the multiplication: {multiplication}')

division = operationA / operationB
print(f'Result of the division: {division}')

Integerdivision = operationA // operationB
print(f'Result of the division integer(int): {Integerdivision}')

exponentiation = operationA ** operationB
print(f'Result of the exponentiation: {exponentiation}')

modulo = operationA % operationB
print(f'Result of the modulo: {modulo}')