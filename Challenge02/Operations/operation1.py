# Assign values to a variable
myVar = 10
print(myVar)

# Form 1
myVar = myVar + 1
print(myVar)

# Form 2
myVar += 1
print(myVar)

# or maybe with subtraction for example:
myVar = myVar - 1
print(myVar)

myVar -= 1
print(myVar)

# They can also be used for multiplication
myVar *= 3
print(myVar)

myVar /= 2
print(myVar)