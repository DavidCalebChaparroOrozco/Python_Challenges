# Comparison operations
numA = 4
numB = 4

result = numA == numB
print(f"The result == : {result}")

result = numA != numB
print(f"The result != : {result}")

result = numA > numB
print(f"The result > : {result}")

result = numA >= numB
print(f"The result >= : {result}")

result = numA < numB
print(f"The result < : {result}")

result = numA <= numB
print(f"The result <= : {result}")